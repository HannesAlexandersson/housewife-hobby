import FloatingElements from "@/components/FloatingElements/FloatingElements";
import Footer from "@/components/Footer/Footer";
import Navbar from "@/components/Navbar/Navbar";
import { ApolloWrapper } from "@/context/ApolloContext";
import LivePreviewProvider from "@/context/LivePreviewProvider";
import { MenuContextProvider } from "@/context/menuContextProvider";
/* import apolloClient from "@/lib/apolloClient";
import previewClient from "@/lib/previewClient"; */
import type { Metadata } from "next";
import { Inter, Questrial } from "next/font/google";
import { draftMode } from "next/headers";
import Script from "next/script";
import "./globals.css";

const questrial = Questrial({
  variable: "--font-rowdies",
  subsets: ["latin"],
  weight: ["400"],
});

const inter = Inter({
  variable: "--font-inter",
  subsets: ["latin"],
  weight: ["500", "700", "800"],
});

export const metadata: Metadata = {
  title: "Mormor och jag",
  description: "Generated by Alexander&Son",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const { isEnabled } = await draftMode();
  //const client = isEnabled ? previewClient : apolloClient;

  return (
    <html
      lang="en"
      className={`${inter.variable} ${questrial.variable} antialiased`}
    >
      <body>
        <MenuContextProvider>
          <LivePreviewProvider isEnabled={isEnabled}>
            <ApolloWrapper>
              <Navbar />
              <FloatingElements>{children}</FloatingElements>
              <Footer />
            </ApolloWrapper>
          </LivePreviewProvider>
        </MenuContextProvider>
      </body>
      {isEnabled && <Script src="/live-preview.mjs" />}
    </html>
  );
}
